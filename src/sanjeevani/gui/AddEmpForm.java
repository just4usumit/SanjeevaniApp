    private void btnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmpActionPerformed
        if(validatesInput())
        {
            try
            {
            EmpPojo e=new EmpPojo();
            double salary=Double.parseDouble(sal);
            e.setEmpid(txtEmpId.getText());
            e.setEmpname(empname);
            e.setSal(salary);
            e.setJob(jcbJob.getName());
            
            if(EmpDao.addEmp(e))
            {
                
                JOptionPane.showMessageDialog(null,"Successfully added employee","Success",JOptionPane.INFORMATION_MESSAGE);
                newEmpId();
            }
            
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null,"SQL Errpr:"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
            catch(NumberFormatException nfe)
            {
                JOptionPane.showMessageDialog(null,"Errpr:"+nfe,"Error",JOptionPane.ERROR_MESSAGE);
                nfe.printStackTrace();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Enter values","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnAddEmpActionPerformed

