    private boolean validatesInput() {
        empname=txtEmpName.getText();
        sal=txtSal.getText();
        return(!(empname.isEmpty()||sal.isEmpty()));
        
    }

    

    private void newEmpId() throws SQLException {
       
        txtEmpId.setText(EmpDao.getNewId());
        txtSal.setText("");
        txtEmpName.setText("");
        jcbJob.setSelectedIndex(0);
        
    }
